#!/bin/sh

f=`basename $0`
subtask=${f%.sh}
s=${f%.sh}
base=`dirname $0`/..

. ./optparams.sh

echo "............................................................................................... "
echo "		1.1 Airport: Number of flights in/out"
echo "............................................................................................... "

echo "............................................................................................... "
echo "		1/2. Execute Hadoop job"
echo "............................................................................................... "

hadoop jar ../jars/$s.jar airline/stage $s $HDP_OPTS

echo "............................................................................................... "
echo "		2/2. Output results"
echo "............................................................................................... "

mkdir -p ${base}/results
hadoop fs -getmerge ${subtask} ${base}/results/${subtask}.csv
cat ${base}/results/${subtask}.csv

echo "............................................................................................... "
echo "		Done"
echo "............................................................................................... "

